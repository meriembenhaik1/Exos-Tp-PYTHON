while True:
    # Demander à l'utilisateur de taper une chaîne
    user_input = input("Veuillez taper une chaîne: ")
    
    # Vérifier si la saisie est vide
    if user_input == "":
        break  # Quitter la boucle
    
    # Afficher la chaîne suivie d'une ligne continue
    print(user_input)
